    <h2>About Us</h2>

    <p>
      OpenOMB is a searchable database that makes apportionments easier to find and track.
      Apportionments are how the president, acting through the Office of Management and Budget,
      implements Congress's spending laws. Apportionments set the pace at which agencies may spend
      appropriated funds over the course of a fiscal year by specifying what money an agency may
      spend, when, and subject to what conditions. OpenOMB aims to make oversight of OMB's
      apportionments easier for Congress, the press, and the public. Drawing on the data files from
      apportionment-public.max.gov, this site makes OMB's apportionments searchable and easier to
      find.
    </p>

